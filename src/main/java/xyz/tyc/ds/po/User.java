package xyz.tyc.ds.po;

/**
 * @author taoyuchu 2014-7-19
 */
public class User {
	private Integer uid;
	private String uname;
	private String upwd;
	private Integer activetimes;
	/**
	 * @return the uid
	 */
	public Integer getUid() {
		return uid;
	}
	/**
	 * @param uid the uid to set
	 */
	public void setUid(Integer uid) {
		this.uid = uid;
	}
	/**
	 * @return the uname
	 */
	public String getUname() {
		return uname;
	}
	/**
	 * @param uname the uname to set
	 */
	public void setUname(String uname) {
		this.uname = uname;
	}
	/**
	 * @return the upwd
	 */
	public String getUpwd() {
		return upwd;
	}
	/**
	 * @param upwd the upwd to set
	 */
	public void setUpwd(String upwd) {
		this.upwd = upwd;
	}
	/**
	 * @return the activetimes
	 */
	public Integer getActivetimes() {
		return activetimes;
	}
	/**
	 * @param activetimes the activetimes to set
	 */
	public void setActivetimes(Integer activetimes) {
		this.activetimes = activetimes;
	}
	
}
